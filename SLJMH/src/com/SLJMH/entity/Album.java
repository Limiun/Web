package com.SLJMH.entity;

public class Album {
	private Integer albumID;
	private String  albumName;
	private String  albumDatetime;
	private String  albumPic;
	private String  albumTag;
	private String  albumuserName;
	private String  albumPermission;
	public Integer getAlbumID() {
		return albumID;
	}
	public void setAlbumID(Integer albumID) {
		this.albumID = albumID;
	}
	public String getAlbumName() {
		return albumName;
	}
	public void setAlbumName(String albumName) {
		this.albumName = albumName;
	}
	public String getAlbumDatetime() {
		return albumDatetime;
	}
	public void setAlbumDatetime(String albumDatetime) {
		this.albumDatetime = albumDatetime;
	}
	public String getAlbumPic() {
		return albumPic;
	}
	public void setAlbumPic(String albumPic) {
		this.albumPic = albumPic;
	}
	public String getAlbumTag() {
		return albumTag;
	}
	public void setAlbumTag(String albumTag) {
		this.albumTag = albumTag;
	}
	public String getAlbumuserName() {
		return albumuserName;
	}
	public void setAlbumuserName(String albumuserName) {
		this.albumuserName = albumuserName;
	}
	public String getAlbumPermission() {
		return albumPermission;
	}
	public void setAlbumPermission(String albumPermission) {
		this.albumPermission = albumPermission;
	}
	
}
