package com.SLJMH.entity;


public class Mood {
	

	private String moodId;

	private String mooduserName;
	
	private String moodTitle;

	
	private String moodDate;

	private String moodContent;
	
	private String moodAddfile;


	public String getMoodAddfile() {
		return moodAddfile;
	}

	public void setMoodAddfile(String moodAddfile) {
		this.moodAddfile = moodAddfile;
	}

	public String getMoodId() {
		return moodId;
	}

	public void setMoodId(String moodId) {
		this.moodId = moodId;
	}

	public String getMooduserName() {
		return mooduserName;
	}

	public void setMooduserName(String mooduserName) {
		this.mooduserName = mooduserName;
	}

	public String getMoodDate() {
		return moodDate;
	}

	public void setMoodDate(String moodDate) {
		this.moodDate = moodDate;
	}

	public String getMoodContent() {
		return moodContent;
	}

	public void setMoodContent(String moodContent) {
		this.moodContent = moodContent;
	}
	
	public String getMoodTitle() {
		return moodTitle;
	}

	public void setMoodTitle(String moodTitle) {
		this.moodTitle = moodTitle;
	}

	

}
